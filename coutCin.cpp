// C++ program to illustrate the take
// multiple input
#include <iostream>
using namespace std;
  
// Driver Code
int main()
{
    string name;
    int age;
  
    // Take multiple input using cin
    cout << "Enter your name below"<< endl;
    cin >> name;
    cout << "Enter your age  below"<< endl;
    cin >> age;
  
    // Print output
    cout << "Name : " << name << endl;
    cout << "Age : " << age << endl;
  
    return 0;
}